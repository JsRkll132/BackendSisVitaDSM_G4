class Usuario : 
    def __init__(self,id_usuario,
                 nombres,
                 ap_paterno,
                 ap_materno,
                 telefono,
                 dni,
                 correo,
                 tipo_usuario
                 ):
        self.id_usuario = id_usuario
        self.nombres = nombres
        self.ap_paterno = ap_paterno
        self.ap_materno = ap_materno
        self.telefono = telefono
        self.dni = dni 
        self.correo = correo,
        self.tipo_usuario = tipo_usuario
     